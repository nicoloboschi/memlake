"""Render bench/results/report.md from the stored per-engine JSON results."""

from __future__ import annotations

from datetime import datetime, timezone

from . import results
from .paths import report_path

ARMS = ("dense", "sparse", "hybrid")


def _fmt(v) -> str:
    return f"{v:.4f}" if isinstance(v, (int, float)) else "-"


def _lat(arm: dict, key: str) -> str:
    v = (arm.get("latency") or {}).get(key)
    return f"{v:.1f}" if isinstance(v, (int, float)) else "-"


def render() -> str:
    data = results.load_all()
    lines: list[str] = [
        "# memlake benchmark results",
        "",
        "Auto-generated by `memlake-bench report` — do not edit by hand.",
        "",
        f"Generated: {datetime.now(timezone.utc).isoformat(timespec='seconds')}",
        "",
        "All engines score the **same query set** against the **same BEIR qrels**",
        "using the **same cached embeddings** (`testdata/embeddings/{dataset}/`),",
        "so dense-arm differences are purely index/ANN effects.",
        "",
    ]

    if not data:
        lines += ["_No results yet. Run `memlake-bench baseline exact <dataset>` first._", ""]
        return "\n".join(lines)

    for dataset in sorted(data):
        engines = data[dataset]
        any_payload = next(iter(engines.values()))
        lines += [
            f"## {dataset}",
            "",
            f"- corpus: {any_payload.get('corpus_size', '?')} docs · "
            f"queries: {any_payload.get('n_queries', '?')}",
            f"- model: `{(any_payload.get('config') or {}).get('model', '?')}` "
            f"(dim {(any_payload.get('config') or {}).get('dim', '?')})",
            "",
            "| Engine | Arm | nDCG@10 | Recall@100 | MRR@10 | ANN r@1 | ANN r@10 | ANN r@100 | p50 ms | p90 ms | p99 ms |",
            "|---|---|--:|--:|--:|--:|--:|--:|--:|--:|--:|",
        ]
        for engine in sorted(engines):
            arms = engines[engine].get("arms", {})
            for arm_name in ARMS:
                arm = arms.get(arm_name)
                if not arm:
                    continue
                lines.append(
                    f"| {engine} | {arm_name} | {_fmt(arm.get('ndcg@10'))} | "
                    f"{_fmt(arm.get('recall@100'))} | {_fmt(arm.get('mrr@10'))} | "
                    f"{_fmt(arm.get('ann_recall@1'))} | {_fmt(arm.get('ann_recall@10'))} | "
                    f"{_fmt(arm.get('ann_recall@100'))} | "
                    f"{_lat(arm, 'p50_ms')} | {_lat(arm, 'p90_ms')} | {_lat(arm, 'p99_ms')} |"
                )
        lines.append("")

        idx = {e: engines[e].get("index_seconds") for e in engines if engines[e].get("index_seconds")}
        if idx:
            lines += [
                "Index build time: "
                + ", ".join(f"{e} {v:.1f}s" for e, v in sorted(idx.items())),
                "",
            ]

    lines += [
        "## Notes",
        "",
        "- `exact` is the reference ceiling: brute-force numpy cosine over the cached",
        "  vectors plus a dependency-free Okapi BM25. It validates the harness itself.",
        "- `exact` sparse is a light-stemming BM25 and scores slightly below Anserini;",
        "  `qdrant` sparse uses fastembed BM25 tokenization with server-side IDF.",
        "- Latencies are single-query, sequential, warm, measured client-side. Qdrant",
        "  numbers therefore include HTTP round-trip and are not directly comparable to",
        "  the in-process `exact` numbers.",
        "",
    ]
    return "\n".join(lines)


def write() -> str:
    path = report_path()
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(render(), encoding="utf-8")
    print(f"[report] wrote {path}")
    return str(path)
